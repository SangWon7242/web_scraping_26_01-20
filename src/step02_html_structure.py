# --------------------------------------------------------------
# 🎨 HTML 속성이란?
# --------------------------------------------------------------
# 속성(Attribute): 태그에 추가 정보를 주는 것
#
# 구조: <태그 속성이름="속성값">내용</태그>
#
# 예시: <a href="https://google.com">구글</a>
#       ↓    ↓
#      태그  속성 (href="주소")

print("=" * 60)
print("🎨 HTML 속성 예시")
print("=" * 60)

attributes_example = """
1. 링크 태그의 href 속성:
   <a href="https://google.com">구글로 이동</a>
   └─ href: 이동할 주소를 지정

2. 이미지 태그의 src 속성:
   <img src="photo.jpg" alt="사진 설명">
   └─ src: 이미지 파일 경로
   └─ alt: 이미지 설명 (이미지가 안 보일 때 표시)

3. class와 id 속성 (매우 중요! ⭐):
   <div class="container">내용</div>
   <p id="main-text">본문</p>
   └─ class: 여러 요소에 같은 이름을 붙일 때
   └─ id: 하나의 요소에 고유한 이름을 붙일 때
"""
print(attributes_example)

