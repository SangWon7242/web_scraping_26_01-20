# --------------------------------------------------------------
# 📚 HTML이란?
# --------------------------------------------------------------
# HTML = HyperText Markup Language (하이퍼텍스트 마크업 언어)
# 
# 쉽게 말하면: 웹페이지의 "뼈대"를 만드는 언어
# 
# 비유: 
#   - HTML = 집의 설계도
#   - 우리가 보는 웹페이지 = 완성된 집
#
# 웹 스크래핑 = 설계도(HTML)를 읽고 원하는 정보를 찾는 것!

# --------------------------------------------------------------
# 🏷️ HTML 태그란?
# --------------------------------------------------------------
# 태그(Tag): HTML에서 내용을 감싸는 표시
# 
# 태그의 기본 구조:
#   <태그이름>내용</태그이름>
#   
#   <시작태그>    내용    </끝태그>
#   ↓             ↓       ↓
#   <h1>       안녕하세요  </h1>

# 예시 HTML을 문자열로 만들어 봅시다
sample_html = """
<!DOCTYPE html>
<html>
<head>
  <title>나의 웹페이지</title>
</head>
<body>
  <h1>안녕하세요!</h1>
  <p>웹 스크래핑을 배우고 있어요.</p>
  <a href="https://www.google.com">구글로 이동</a>
</body>
</html>
"""
print("=" * 60)
print("📄 예시 HTML 코드")
print("=" * 60)
print(sample_html)

