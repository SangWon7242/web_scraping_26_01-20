tree_structure = """
HTML은 나무(Tree) 구조를 가집니다!

<html>                    ← 뿌리 (root)
  │
  ├── <head>              ← html의 자식
  │     └── <title>       ← head의 자식
  │
  └── <body>              ← html의 자식
        ├── <h1>          ← body의 자식
        ├── <p>           ← body의 자식
        └── <div>         ← body의 자식
              └── <a>     ← div의 자식

💡 용어:
   - 부모(Parent): 바로 위에 있는 태그
   - 자식(Child): 바로 아래에 있는 태그
   - 형제(Sibling): 같은 부모를 가진 태그들
"""
print(tree_structure)

